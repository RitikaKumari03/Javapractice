package org.example.Operators;

    public class UnaryOperator {
        public static void main(String[] args){
            int number = 10;
            System.out.println(number);
            System.out.println(number++);  //11
            System.out.println(number);
            System.out.println(++number);
            int number1=5;
            System.out.println(number1);
            System.out.println(number1--);
            System.out.println(number1);
            System.out.println(--number1);
        }

    }


